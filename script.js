var age = prompt('Введите свой возраст')


if (age <= 18) {
    alert('Вы еще молоды. Вам нужно учиться');
} else if (age <= 50) {
    alert('Вам нужно работать');
} else if (age <= 59) {
    alert('Вам скоро в пенсию');
} else if (age <= 150) {
    alert('Вы пенсионер');
} else if (age >= 150) {
    alert('Что-то пошло не так')
}













